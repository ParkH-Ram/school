package _19_Network;

import java.io.IOException;
import java.net.ServerSocket;
import java.net.Socket;
import java.util.Collection;
import java.util.Collections;
import java.util.HashMap;
import java.util.Map;
import java.util.Scanner;
import java.util.concurrent.ExecutorService;
import java.util.concurrent.Executors;

import org.json.JSONObject;


public class _7_ChatServer {
	//필드
	ServerSocket serverSocket;
	ExecutorService threadPool = Executors.newFixedThreadPool(100);
	Map<String, SocketClient> chatRoom = Collections.synchronizedMap(new HashMap<>());
	public static void main(String[]args) {
		try {
			_7_ChatServer chatServer = new _7_ChatServer();
			chatServer.start();
			
			System.out.println("-------------------------------------");
			System.out.println("서버를 종료하려면 q를 입력 하고 enter");
			System.out.println("-------------------------------------");
			
			Scanner hi = new Scanner(System.in);
			while (true) {
				String key = hi.nextLine();
				if(key.equals("q")) break;
			}
			hi.close();
			chatServer.stop();
		}catch(Exception e) {
			System.err.println(e.getMessage());
		}
	}
	//메소드 : 메인

	//메서드 시작
	public void start()throws IOException{
		serverSocket = new ServerSocket(50001);
		System.out.println("서버 : 시작");
		
		Thread thread = new Thread(()->  {
			try {
				while(true) {
					Socket socket = serverSocket.accept();
					SocketClient sc = new SocketClient(this, socket);   // 세미콜론 잘 확인...
				}
			}catch (Exception e) {
				System.err.println(e.getMessage());
			}
		});
		thread.start();
	}
	//메소드 : 클라이언트 연결 시 SocketClient 생성 및 추가
	public void addSocketClient(SocketClient socketClient) {
		String key = socketClient.chatName + "@" + socketClient.clientIp;
		chatRoom.put(key, socketClient);
		System.out.println("입장 : " + key);
		System.out.println("현 재 인 원 수  : "  + chatRoom.size() + "\n");
	}
	
	//메소드 : 클라이언트 연결 종료 시 SoketClient 제거
	public void removeSocketClient(SocketClient socketClient) {
		String key = socketClient.chatName + "@" + socketClient.clientIp;
		chatRoom.remove(key);
		System.out.println("get out here: " + key);
		System.out.println("현 재 채 팅 자 수 : " + chatRoom.size() + "\n");
	}
	//메소드  : 모든 클라이언트에게 메시지 보냄
	public void sendToAll(SocketClient sender, String message) {
		JSONObject root = new JSONObject();
		root.put("clientIp", sender.clientIp);
		root.put("chatName", sender.chatName);
		root.put("message", message);
		String json = root.toString();
		
		Collection<SocketClient> socketClients = chatRoom.values();
		for(SocketClient sc : socketClients) {
			if (sc == sender) continue;
			sc.send(json);
		}
	}
	//서버 종료
	public void stop() {
		try {
			serverSocket.close();
			threadPool.shutdownNow();
			chatRoom.values().stream().forEach(sc -> sc.close());
			System.out.println("서버 : 종료");
		}catch (Exception e1) {
			System.err.println(e1.getMessage());
		}

	}
}
 