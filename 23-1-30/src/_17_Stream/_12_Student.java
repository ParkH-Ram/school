package _17_Stream;

public class _12_Student {
	private String name;
	private String gender;
	private int score;

	
	public _12_Student(String name, String gender, int score) {
		this.name=name;
		this.gender=gender;
		this.score=score;
	}

	public String getName() {
		return name;
	}

	public String getGender() {
		return gender;
	}

	public int getScore() {
		return score;
	}

}
