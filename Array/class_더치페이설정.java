package Array;

public class class_더치페이설정 {
	
			// 메소드 이름 설정 ( 메소드 이름 설정 )
	public static int dutch(int totalpay, int person) {
			
			// 동작할 메소드 이름 및 연산 설정
		int dutchpay = totalpay/person;
			
			// 반환
			return dutchpay;
		
	}
	public static void main(String[]args) {

		// 작업 변수 생성	
		
		int dutchpay1;
		
		// 변수에 작성한 메소드 설정 (totalpay, person)
		dutchpay1 = dutch(50000, 5);
	
		// 출력
		System.out.print(dutchpay1);
		
		
 }
}

